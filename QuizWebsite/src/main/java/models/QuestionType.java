package models;

/**
 * This class defines constants for different types of questions.
 */
public class QuestionType {

    /**
     * Represents a question type where the user is required to fill in the blank space.
     */
    public static final String FILL_THE_BLANK = "fill_the_blank";

    /**
     * Represents a question type where the user is expected to provide a written response.
     */
    public static final String QUESTION_RESPONSE = "question_response";

    /**
     * Represents a question type where the user selects one option from multiple choices.
     */
    public static final String MULTIPLE_CHOICE = "multiple_choice";

    /**
     * Represents a question type that involves a picture-based question.
     */
    public static final String PICTURE_QUESTION = "picture_question";
}
